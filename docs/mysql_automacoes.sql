CREATE TABLE automacoes_tarefas_modelos (
  id_automacao_tarefa_modelo BIGINT PRIMARY KEY AUTO_INCREMENT,
  tenant_id BIGINT NOT NULL,
  nome_modelo VARCHAR(150) NOT NULL,
  modulo VARCHAR(30) NOT NULL,
  tipo_local VARCHAR(20) NULL,
  id_obra BIGINT NULL,
  id_unidade BIGINT NULL,
  id_setor_diretoria BIGINT NULL,
  titulo_tarefa VARCHAR(180) NOT NULL,
  descricao_tarefa TEXT NULL,
  recorrencia VARCHAR(20) NOT NULL,
  horario_execucao TIME NOT NULL,
  timezone VARCHAR(60) NOT NULL DEFAULT 'America/Sao_Paulo',
  dia_semana TINYINT NULL,
  dia_mes TINYINT NULL,
  responsavel_tipo VARCHAR(30) NOT NULL,
  id_usuario_responsavel BIGINT NULL,
  permissao_responsavel VARCHAR(120) NULL,
  gera_notificacao TINYINT(1) NOT NULL DEFAULT 1,
  gera_email TINYINT(1) NOT NULL DEFAULT 0,
  ativo TINYINT(1) NOT NULL DEFAULT 1,
  proxima_execucao_em DATETIME NULL,
  ultima_execucao_em DATETIME NULL,
  criado_em DATETIME NOT NULL DEFAULT CURRENT_TIMESTAMP,
  atualizado_em DATETIME NOT NULL DEFAULT CURRENT_TIMESTAMP ON UPDATE CURRENT_TIMESTAMP,
  INDEX ix_auto_mod_tenant (tenant_id, modulo, ativo),
  INDEX ix_auto_next (tenant_id, proxima_execucao_em, ativo)
);

