  atribuido_em DATETIME NULL,
  iniciada_em DATETIME NULL,
  concluida_em DATETIME NULL,
  concluida_por_usuario BIGINT NULL,
  origem_entidade_tipo VARCHAR(60) NULL,
  origem_entidade_id BIGINT NULL,
  observacao_conclusao TEXT NULL,
  criado_em DATETIME NOT NULL DEFAULT CURRENT_TIMESTAMP,
