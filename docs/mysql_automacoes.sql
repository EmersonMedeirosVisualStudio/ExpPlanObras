CREATE TABLE automacoes_execucoes (
  id_automacao_execucao BIGINT PRIMARY KEY AUTO_INCREMENT,
  tenant_id BIGINT NOT NULL,
  tipo_execucao VARCHAR(20) NOT NULL,
  status_execucao VARCHAR(20) NOT NULL DEFAULT 'PENDENTE',
  id_usuario_executor_manual BIGINT NULL,
  execucao_manual TINYINT(1) NOT NULL DEFAULT 0,
  iniciado_em DATETIME NULL,
  finalizado_em DATETIME NULL,
  total_processado INT NOT NULL DEFAULT 0,
  total_criado INT NOT NULL DEFAULT 0,
  total_notificado INT NOT NULL DEFAULT 0,
  total_escalado INT NOT NULL DEFAULT 0,
  mensagem_resultado TEXT NULL,
  criado_em DATETIME NOT NULL DEFAULT CURRENT_TIMESTAMP,
  INDEX ix_execucoes_tenant (tenant_id, criado_em),
  INDEX ix_execucoes_status (tenant_id, status_execucao, criado_em)
);

